// ─── 최근접 데이터 포인트 조회 ────────────────────────────────────────────────
export type GrowthRowMatch = {
  row: GrowthPercentile
  exact: boolean        // 기록 월령과 참고 데이터 월령이 일치
  nearestMonth: number  // 실제 참고에 사용된 월령 (37개월 → 36 또는 42)
}

/**
 * 입력 월령에 가장 가까운 성장 참고 행을 반환합니다.
 * 37~84개월처럼 6개월 간격 데이터만 있는 구간도 결과를 반환합니다.
 * 일치하지 않으면 exact=false, nearestMonth에 실제 참고 월령이 담깁니다.
 */
export function findNearestGrowthRow(
  data: GrowthPercentile[],
  monthsOld: number,
): GrowthRowMatch | null {
  if (data.length === 0) return null
  const nearest = data.reduce((prev, curr) =>
    Math.abs(curr.month - monthsOld) < Math.abs(prev.month - monthsOld) ? curr : prev
  )
  return {
    row: nearest,
    exact: nearest.month === monthsOld,
    nearestMonth: nearest.month,
  }
}
